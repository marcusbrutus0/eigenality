//! `_redirects` file generation.
//!
//! Priority:
//! 1. `static/_redirects` present + config rules empty  → copy static file to dist.
//! 2. `static/_redirects` present + config rules non-empty → bail (conflict error).
//! 3. No static file + config rules empty               → no-op (no file written).
//! 4. No static file + config rules non-empty          → generate from config.

use eyre::{Result, WrapErr, bail};
use std::path::Path;

use crate::config::{RedirectRule, SiteConfig};

/// Write `dist/_redirects` according to the priority rules.
pub fn write(project_root: &Path, dist_dir: &Path, config: &SiteConfig) -> Result<()> {
    let static_file = project_root.join("static").join("_redirects");
    let dest = dist_dir.join("_redirects");

    if static_file.exists() {
        if config.redirects.is_empty() {
            std::fs::copy(&static_file, &dest)
                .wrap_err_with(|| format!("Failed to copy _redirects to {}", dest.display()))?;
            tracing::info!("Copying _redirects file... ✓");
        } else {
            bail!(
                "Both `static/_redirects` and `[[redirects]]` rules in site.toml are present. \
                 Remove one. Redirect rule ordering is load-bearing — silently merging them \
                 would produce incorrect site behaviour."
            );
        }
        return Ok(());
    }

    if config.redirects.is_empty() {
        return Ok(());
    }

    let content = build_redirects_content(&config.redirects);
    std::fs::write(&dest, &content)
        .wrap_err_with(|| format!("Failed to write _redirects to {}", dest.display()))?;
    tracing::info!(
        "Generating _redirects ({} rule(s))... ✓",
        config.redirects.len()
    );

    Ok(())
}

/// Build the `_redirects` file content from a slice of rules.
fn build_redirects_content(rules: &[RedirectRule]) -> String {
    if rules.is_empty() {
        return String::new();
    }

    let mut out = String::from("# Generated by eigen — do not edit by hand.\n");
    for rule in rules {
        out.push_str(&rule.from);
        out.push(' ');
        out.push_str(&rule.to);
        out.push(' ');
        out.push_str(&rule.status.to_string());
        out.push('\n');
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::config::{
        BuildConfig, RedirectRule, SiteConfig, SiteMeta, SiteSchemaConfig, SiteSeoConfig,
        SitemapConfig,
    };
    use std::collections::HashMap;
    use std::fs;
    use tempfile::TempDir;

    fn make_config(rules: Vec<RedirectRule>) -> SiteConfig {
        SiteConfig {
            site: SiteMeta {
                name: "Test".into(),
                base_url: "https://example.com".into(),
                seo: SiteSeoConfig::default(),
                schema: SiteSchemaConfig::default(),
                extra: HashMap::new(),
            },
            build: BuildConfig::default(),
            sitemap: SitemapConfig::default(),
            robots: Default::default(),
            assets: Default::default(),
            sources: HashMap::new(),
            analytics: None,
            plugins: HashMap::new(),
            feed: HashMap::new(),
            audit: None,
            security_headers: Default::default(),
            redirects: rules,
        }
    }

    fn setup(static_redirects: Option<&str>) -> TempDir {
        let tmp = TempDir::new().unwrap();
        fs::create_dir_all(tmp.path().join("dist")).unwrap();
        if let Some(content) = static_redirects {
            fs::create_dir_all(tmp.path().join("static")).unwrap();
            fs::write(tmp.path().join("static/_redirects"), content).unwrap();
        }
        tmp
    }

    fn rule(from: &str, to: &str, status: u16) -> RedirectRule {
        RedirectRule {
            from: from.into(),
            to: to.into(),
            status,
        }
    }

    // --- build_redirects_content ---

    #[test]
    fn test_comment_header() {
        let content = build_redirects_content(&[rule("/old", "/new", 301)]);
        assert!(content.starts_with("# Generated by eigen"));
    }

    #[test]
    fn test_basic_rule() {
        let content = build_redirects_content(&[rule("/old", "/new", 301)]);
        assert!(content.contains("/old /new 301\n"));
    }

    #[test]
    fn test_default_status() {
        // 301 is the serde default for status (via default_redirect_status).
        let content = build_redirects_content(&[rule("/a", "/b", 301)]);
        assert!(content.contains("/a /b 301\n"));
    }

    #[test]
    fn test_302_rule() {
        let content = build_redirects_content(&[rule("/old", "/new", 302)]);
        assert!(content.contains("/old /new 302\n"));
    }

    #[test]
    fn test_307_308_rules() {
        let content = build_redirects_content(&[rule("/a", "/b", 307), rule("/c", "/d", 308)]);
        assert!(content.contains("/a /b 307\n"));
        assert!(content.contains("/c /d 308\n"));
    }

    #[test]
    fn test_order_preserved() {
        let content = build_redirects_content(&[
            rule("/first", "/f", 301),
            rule("/second", "/s", 302),
            rule("/third", "/t", 308),
        ]);
        let first_pos = content.find("/first").unwrap();
        let second_pos = content.find("/second").unwrap();
        let third_pos = content.find("/third").unwrap();
        assert!(first_pos < second_pos && second_pos < third_pos);
    }

    #[test]
    fn test_external_from() {
        let content = build_redirects_content(&[rule("https://old.com/*", "/new", 301)]);
        assert!(content.contains("https://old.com/* /new 301\n"));
    }

    #[test]
    fn test_external_to() {
        let content = build_redirects_content(&[rule("/old", "https://new.com", 301)]);
        assert!(content.contains("/old https://new.com 301\n"));
    }

    #[test]
    fn test_placeholder_passthrough() {
        let content = build_redirects_content(&[rule("/posts/:slug", "/blog/:slug", 301)]);
        assert!(content.contains("/posts/:slug /blog/:slug 301\n"));

        let content2 = build_redirects_content(&[rule("/old/*", "/new/:splat", 301)]);
        assert!(content2.contains("/old/* /new/:splat 301\n"));
    }

    #[test]
    fn test_empty_rules() {
        let content = build_redirects_content(&[]);
        assert!(content.is_empty());
    }

    // --- write (integration with filesystem) ---

    #[test]
    fn test_static_file_copied_when_rules_empty() {
        let tmp = setup(Some("/legacy /new 301\n"));
        let config = make_config(vec![]);
        write(tmp.path(), &tmp.path().join("dist"), &config).unwrap();
        let content = fs::read_to_string(tmp.path().join("dist/_redirects")).unwrap();
        assert_eq!(content, "/legacy /new 301\n");
    }

    #[test]
    fn test_conflict_error_when_both_present() {
        let tmp = setup(Some("/old /new 301\n"));
        let config = make_config(vec![rule("/a", "/b", 301)]);
        let result = write(tmp.path(), &tmp.path().join("dist"), &config);
        assert!(result.is_err());
        assert!(result.unwrap_err().to_string().contains("site.toml"));
    }

    #[test]
    fn test_no_op_when_no_static_and_no_rules() {
        let tmp = setup(None);
        let config = make_config(vec![]);
        write(tmp.path(), &tmp.path().join("dist"), &config).unwrap();
        assert!(!tmp.path().join("dist/_redirects").exists());
    }

    #[test]
    fn test_generated_from_rules() {
        let tmp = setup(None);
        let config = make_config(vec![
            rule("/old", "/new", 301),
            rule("/gone", "/replacement", 302),
        ]);
        write(tmp.path(), &tmp.path().join("dist"), &config).unwrap();
        let content = fs::read_to_string(tmp.path().join("dist/_redirects")).unwrap();
        assert!(content.starts_with("# Generated by eigen"));
        assert!(content.contains("/old /new 301\n"));
        assert!(content.contains("/gone /replacement 302\n"));
    }
}
